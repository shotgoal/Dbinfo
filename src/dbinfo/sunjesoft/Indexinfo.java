package dbinfo.sunjesoft;

public class Indexinfo {

	String idx_name;
	String idx_type;
	String idx_colname;
	String idx_comment;
	
	public String getIdx_name() {
		return idx_name;
	}
	public void setIdx_name(String idx_name) {
		this.idx_name = idx_name;
	}
	public String getIdx_type() {
		return idx_type;
	}
	public void setIdx_type(String idx_type) {
		this.idx_type = idx_type;
	}
	public String getIdx_colname() {
		return idx_colname;
	}
	public void setIdx_colname(String idx_colname) {
		this.idx_colname = idx_colname;
	}
	public String getIdx_comment() {
		return idx_comment;
	}
	public void setIdx_comment(String idx_comment) {
		this.idx_comment = idx_comment;
	}
}
